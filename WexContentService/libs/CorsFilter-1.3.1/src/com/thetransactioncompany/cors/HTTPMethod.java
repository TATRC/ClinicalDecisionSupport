package com.thetransactioncompany.cors;


/**
 * Enumeration of all standard HTTP methods.
 *
 * @author <a href="http://dzhuvinov.com">Vladimir Dzhuvinov</a>
 * @version 1.3.1 (2010-09-23)
 */
public enum HTTPMethod {

	
	/**
	 * HTTP GET.
	 */
	GET,
	
	
	/**
	 * HTTP POST.
	 */
	POST,
	
	
	/**
	 * HTTP HEAD.
	 */
	HEAD,
	
	
	/**
	 * HTTP PUT.
	 */
	PUT,
	
	
	/**
	 * HTTP DELETE.
	 */
	DELETE,
	
	
	/**
	 * HTTP TRACE.
	 */
	TRACE,
	
	
	/**
	 * HTTP OPTIONS.
	 */
	OPTIONS,
	
	
	/**
	 * HTTP CONNECT.
	 */
	CONNECT,
	
	
	/**
	 * HTTP PATCH.
	 */
	PATCH


}
