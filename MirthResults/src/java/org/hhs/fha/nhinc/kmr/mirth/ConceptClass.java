package org.hhs.fha.nhinc.kmr.mirth;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;

/**
 *
 * @author Duane DeCouteau
 */
@Entity
@Table(name = "concept_class")
@NamedQueries({@NamedQuery(name = "ConceptClass.findAll", query = "SELECT c FROM ConceptClass c"), @NamedQuery(name = "ConceptClass.findById", query = "SELECT c FROM ConceptClass c WHERE c.id = :id"), @NamedQuery(name = "ConceptClass.findByLabel", query = "SELECT c FROM ConceptClass c WHERE c.label = :label")})
public class ConceptClass implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @Column(name = "id")
    private String id;
    @Basic(optional = false)
    @Column(name = "label")
    private String label;

    public ConceptClass() {
    }

    public ConceptClass(String id) {
        this.id = id;
    }

    public ConceptClass(String id, String label) {
        this.id = id;
        this.label = label;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof ConceptClass)) {
            return false;
        }
        ConceptClass other = (ConceptClass) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "org.hhs.fha.nhinc.kmr.mirth.ConceptClass[id=" + id + "]";
    }

}
